@router.message(Command("status"))
async def cmd_status(message: types.Message, bot: Bot) -> None:
    """–ö–æ–º–∞–Ω–¥–∞ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å—Ç–∞—Ç—É—Å–∞ –±–æ—Ç–∞ –≤ —á–∞—Ç–µ."""
    if message.chat.type == ChatType.PRIVATE:
        await message.answer(
            "‚ÑπÔ∏è –≠—Ç–∞ –∫–æ–º–∞–Ω–¥–∞ —Ä–∞–±–æ—Ç–∞–µ—Ç —Ç–æ–ª—å–∫–æ –≤ –≥—Ä—É–ø–ø–æ–≤—ã—Ö —á–∞—Ç–∞—Ö."
        )
        return

    chat_id = message.chat.id
    chat = await get_chat_from_db(chat_id)

    bot_is_admin = await is_bot_admin(chat_id, bot)

    status_text = "üìä <b>–°—Ç–∞—Ç—É—Å –±–æ—Ç–∞ –≤ —á–∞—Ç–µ</b>\n\n"

    if chat and chat.is_active:
        status_text += "‚úÖ –ë–æ—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω\n"
    else:
        status_text += "‚ùå –ë–æ—Ç –Ω–µ –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω (–∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ /setup)\n"

    if bot_is_admin:
        status_text += "‚úÖ –ë–æ—Ç —è–≤–ª—è–µ—Ç—Å—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–º\n"
    else:
        status_text += "‚ö†Ô∏è –ë–æ—Ç –ù–ï —è–≤–ª—è–µ—Ç—Å—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–º\n"

    await message.answer(status_text, parse_mode="HTML")
